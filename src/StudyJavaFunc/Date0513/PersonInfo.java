package StudyJavaFunc.Date0513;

public class PersonInfo {
    public String name;
    public double height;
    public double weight;
    public int age;

    public PersonInfo() {

    }

    public PersonInfo(String name, double height, double weight, int age) {
        this.name = name;
        this.height = height;
        this.weight = weight;
        this.age = age;
    }
}
